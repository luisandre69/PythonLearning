#%% List.insert(i,x)
L=[1,2,3]
L.insert(0,100)
L   #[100,1,2,3]
#%% List.append(x)
L=[100,1,2,3]
L.append(500)
L   #[100,1,2,3,500]
#%% List.remove(x)
L=[100,1,2,3,500]
L.remove(500)
L   #[100,1,2,3]
#%% List.pop([i])
L=[100,1,2,3]
L.pop(1)
L   #[100,2,3] removes i posiiton
#%% List.pop()
L=[100,1,2,3]
L.pop()
L   #[100,1,2] removes last element
